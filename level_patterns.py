def get_pattern(level):
    # Returns a pattern for the given level. 
    # If level > 20, generates a unique procedural tech-pattern.
    if level in LEVEL_PATTERNS:
        return LEVEL_PATTERNS[level]
    
    # Procedural Generation for Advanced Levels (21-100)
    import random
    # Use level as seed for consistency if needed, but here we want variety
    rows = 7 + min(level // 10, 3)
    cols = 10
    pattern = []
    
    # Fill density increases with level
    density = 0.35 + min(level / 100, 0.45) 
    
    # STYLE SELECTION
    style = level % 3 # 0: Symmetric, 1: Checkerboard, 2: Frame
    
    for r in range(rows):
        row = []
        for c in range(cols):
            if style == 0: # Tech Symmetry
                if c < cols // 2:
                    val = 1 if random.random() < density else 0
                    row.append(val)
                else:
                    row.append(row[cols - 1 - c])
            elif style == 1: # Checkerboard Tech
                if (r + c) % 2 == 0 and random.random() < (density * 1.5):
                    row.append(1)
                else:
                    row.append(0)
            else: # Frame/Hollow Structure
                if r == 0 or r == rows-1 or c == 0 or c == cols-1:
                    row.append(1 if random.random() < 0.8 else 0)
                else:
                    row.append(1 if random.random() < (density * 0.5) else 0)
        
        if style != 0: # Ensure some symmetry anyway for looks
             pattern.append(row)
        else:
             pattern.append(row)
             
    return pattern

